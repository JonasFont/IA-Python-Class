#------------------------------------------BOOL---------------------------------------

#Em Python, uma variável pode assumir um valor booleano de True (verdadeiro) ou False (falso). 
#Esses valores são úteis para representar o resultado de comparações ou expressões lógicas. 
#Aqui estão algumas informações importantes sobre variáveis booleanas:

#Declaração de Variáveis Booleanas: Para declarar uma variável booleana em Python, 
#você simplesmente atribui o valor True ou False a um nome de variável. Por exemplo:

x = True
y = False

#Operadores Lógicos: Você também pode usar operadores 
#booleanos para combinar ou negar valores booleanos. Alguns operadores lógicos comuns são:

#and: Retorna True se ambos os operandos forem verdadeiros.
#or: Retorna True se pelo menos um dos operandos for verdadeiro.
#not: Inverte o valor booleano (se for True, torna-se False, e vice-versa).

a = True
b = False
resultado_and = a and b  # False
resultado_or = a or b    # True
resultado_not = not a    # False
